package ch6methods;

public class MathFunction {
    public static void main(String[] args) {


        System.out.println(Math.abs(-7.5));
        System.out.println(Math.floor(5+2.5));
        System.out.println(Math.abs(9)+Math.ceil(2.2));
        System.out.println(Math.ceil(-5.2));
        System.out.println(Math.abs(4)+Math.abs(-5));
        System.out.println(Math.ceil(-6.4)-Math.floor(5.2));
        System.out.println(Math.ceil(-Math.abs(-3+Math.floor(-2.5))));


    }
}
